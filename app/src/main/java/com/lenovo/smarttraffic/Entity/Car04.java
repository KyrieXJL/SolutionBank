package com.lenovo.smarttraffic.Entity;

public class Car04 {
    private String name;
    private int id;

    public Car04(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
